    private void ItemsComboMedicos(List<domain.Medico> medicos){
        comboBoxMedicos.removeAllItems();

        if(medicos.isEmpty()){
            return;
        }

        medicos.forEach(medico -> {
            comboBoxMedicos.addItem(medico.getNombre());
        });
        comboBoxMedicos.setSelectedIndex(0);
    }

